package com.pos.accounts.model;

public class SupplierInfo {

	private String supplierId;
	private String encSupplierId;
	private String supplierName;
	private String supplierAddress;
	private String mobileNo;
	private String enableYn;
	private String updateBy;
	private String updateDate;
	private String mCode;
	private String message;
	
	
	public String getSupplierId() {
		return supplierId;
	}
	public void setSupplierId(String supplierId) {
		this.supplierId = supplierId;
	}
	public String getEncSupplierId() {
		return encSupplierId;
	}
	public void setEncSupplierId(String encSupplierId) {
		this.encSupplierId = encSupplierId;
	}
	public String getSupplierName() {
		return supplierName;
	}
	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}
	public String getSupplierAddress() {
		return supplierAddress;
	}
	public void setSupplierAddress(String supplierAddress) {
		this.supplierAddress = supplierAddress;
	}
	public String getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}
	public String getEnableYn() {
		return enableYn;
	}
	public void setEnableYn(String enableYn) {
		this.enableYn = enableYn;
	}
	public String getUpdateBy() {
		return updateBy;
	}
	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}
	public String getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	public String getmCode() {
		return mCode;
	}
	public void setmCode(String mCode) {
		this.mCode = mCode;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	
	
}
